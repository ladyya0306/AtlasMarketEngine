let currentLang = loadLang();

function loadLang() {
  try {
    const saved = window.localStorage.getItem(LANG_KEY);
    return saved === "en" ? "en" : "zh";
  }
  catch {
    return "zh";
  }
}

export function getLang() {
  return currentLang;
}

export function isEnglish() {
  return currentLang === "en";
}

export function setLang(lang) {
  currentLang = lang === "en" ? "en" : "zh";
  try {
    window.localStorage.setItem(LANG_KEY, currentLang);
  }
  catch {
    // Ignore storage failure.
  }
}

export function t(key, vars = {}) {
  const table = dict[currentLang] || dict.zh;
  const fallbackTable = dict.en;
  const template = table[key] ?? fallbackTable[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
}

export function translateStatus(status) {
  return t(`status.${status || "idle"}`);
}

export function translatePhase(phase) {
  return t(`phase.${phase || "system"}`);
}

export function translateBool(value) {
  return value ? t("common.on") : t("common.off");
}

export function monthLabel(month) {
  return t("common.month_full", { month });
}

export function monthShort(month) {
  return t("common.month_short", { month });
}

export function applyStaticI18n(root = document) {
  if (!root) {
    return;
  }
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (!key) {
      return;
    }
    if (node.tagName === "TITLE") {
      document.title = t(key);
      return;
    }
    node.textContent = t(key);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.getAttribute("data-i18n-placeholder");
    if (!key) {
      return;
    }
    node.setAttribute("placeholder", t(key));
  });
}

