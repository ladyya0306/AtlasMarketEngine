export function localizeNarrativeText(text) {
  const source = String(text || "");
  if (!source) {
    return source;
  }
  if (currentLang === "en") {
    return source;
  }
  let output = source;
  for (const [pattern, replacement] of narrativeReplacementsZh) {
    output = output.replace(pattern, replacement);
  }
  output = output
    .replace(/\bMonth\b/gi, "第")
    .replace(/\bStatus\b/gi, "状态")
    .replace(/\bReason\b/gi, "原因")
    .replace(/\bCount\b/gi, "数量")
    .replace(/\bUpdated\b/gi, "已更新")
    .replace(/\bStarted\b/gi, "开始")
    .replace(/\bProgress\b/gi, "推进")
    .replace(/\bClosed\b/gi, "收束")
    .replace(/\bCollapsed\b/gi, "破裂")
    .replace(/\bTurn\b/gi, "回合")
    .replace(/\bBatch End\b/gi, "批次结束");
  return output;
}
